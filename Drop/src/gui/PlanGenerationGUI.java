    private void btn_MaintenanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_MaintenanceActionPerformed
        if (!controller.startMaintenancePlanGeneration())
            JOptionPane.showMessageDialog(this, "Could not initiate a connection with the DataBase.\nPlease try again later.", "Connection Error", JOptionPane.ERROR_MESSAGE);
        else
            txt_StartingList.setText(controller.getStartingList());
    }//GEN-LAST:event_btn_MaintenanceActionPerformed

    private void btn_GenerateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_GenerateActionPerformed
        txt_GeneratedList.setText(controller.getPlannedList());
    }//GEN-LAST:event_btn_GenerateActionPerformed

    private void btn_QuitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_QuitActionPerformed
        this.dispose();
    }//GEN-LAST:event_btn_QuitActionPerformed

    private void btn_RepairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_RepairActionPerformed
        if (!controller.startRepairPlanGeneration())
            JOptionPane.showMessageDialog(this, "Could not initiate a connection with the DataBase.\nPlease try again later.", "Connection Error", JOptionPane.ERROR_MESSAGE);
        else
            txt_StartingList.setText(controller.getStartingList());
    }//GEN-LAST:event_btn_RepairActionPerformed

    private void btn_SubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SubmitActionPerformed
        controller.submitPlan();
        JOptionPane.showMessageDialog(this, "Submitted successfully!", "Plan Submission", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btn_SubmitActionPerformed

