    private void sair() {
        String[] options = new String[2];
        options[0] = "Sim";
        options[1] = "NÃ£o";
        int op = JOptionPane.showOptionDialog(this, "Deseja cancelar a compra de DropPoint?", "Cancelar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
        if (op == 0) {
            dispose();
        }
    }
